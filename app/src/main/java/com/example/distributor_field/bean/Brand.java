package com.example.distributor_field.bean;

public class Brand {
    String brandId;
    String brandName;

    public String getBrandId() {
        return brandId;
    }

    public Brand setBrandId(String brandId) {
        this.brandId = brandId;
        return this;
    }

    public String getBrandName() {
        return brandName;
    }

    public Brand setBrandName(String brandName) {
        this.brandName = brandName;
        return this;
    }
}
